public class Movie{
    int id;
    String title;
    double duration;
    String showTime;
    String showDate;
   

    public Movie(int id, String title, double duration, String showTime, String showDate){
        this.id = id;
        this.title = title;
        this.duration = duration;
        this.showTime = showTime;
        this.showDate = showDate;
    }

    
}